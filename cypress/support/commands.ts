// Declare a namespace within Cypress to extend its functionality
declare namespace Cypress {
  // Extend the Chainable interface to include a custom command
  interface Chainable {
    // Custom command for selecting elements by data-test attribute
    getByData(dataTestAttribute: string): Chainable<JQuery<HTMLElement>>
  }
}

// Define the custom Cypress command getByData
Cypress.Commands.add("getByData", (selector) => {
  // Use cy.get to select elements by data-test attribute
  return cy.get(`[data-test=${selector}]`)
})
